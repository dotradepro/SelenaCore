    async def _claim_intent_ownership(self) -> None:
        """Ensure every OWNED_INTENT exists in the DB and is owned by us.

        Creates missing definitions on the fly using ``_OWNED_INTENT_META``.
        This makes the module self-sufficient: it does not rely on
        ``scripts/seed_intents_to_db.py`` to populate ``intent_definitions``
        rows. Existing rows have their ``module`` column rewritten to
        ``device-control`` so any duplicates from older code paths land
        under our ownership.
        """
        if self._session_factory is None:
            return
        from core.registry.models import IntentDefinition
        import json as _json

        async with self._session_factory() as session:
            # Existing rows → claim ownership.
            await session.execute(
                update(IntentDefinition)
                .where(IntentDefinition.intent.in_(OWNED_INTENTS))
                .values(module=self.name)
            )

            # Sync entity_types on existing rows every boot so the DB
            # always reflects the module's current declared support list.
            # This is how new device types become valid for an intent —
            # the module updates its META and a restart rewrites the DB.
            for intent_name, meta in self._OWNED_INTENT_META.items():
                ent = meta.get("entity_types")
                payload = _json.dumps(list(ent)) if ent else None
                await session.execute(
                    update(IntentDefinition)
                    .where(IntentDefinition.intent == intent_name)
                    .values(
                        entity_types=payload,
                        description=meta["description"],
                    )
                )

            # Missing rows → insert with module-defined defaults.
            existing = {
                row[0] for row in (await session.execute(
                    select(IntentDefinition.intent).where(
                        IntentDefinition.intent.in_(OWNED_INTENTS)
                    )
                )).all()
            }
            for intent_name in OWNED_INTENTS:
                if intent_name in existing:
                    continue
                meta = self._OWNED_INTENT_META.get(intent_name)
                if meta is None:
                    continue
                ent = meta.get("entity_types")
                session.add(IntentDefinition(
                    intent=intent_name,
                    module=self.name,
                    noun_class=meta["noun_class"],
                    verb=meta["verb"],
                    priority=meta["priority"],
                    description=meta["description"],
                    source="module",
                    entity_types=_json.dumps(list(ent)) if ent else None,
                ))
            await session.commit()
